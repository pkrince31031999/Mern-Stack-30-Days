// Math objects


// absolute

console.log(Math.abs(12))       // 12

console.log(Math.abs(-12))     // 12



// Power 

console.log(Math.pow(2,3))      // 8



// floor

console.log(Math.floor(3.99))   // 3

console.log(Math.floor(-3.99))  // -4



// ceil

console.log(Math.ceil(3.99))    // 4

console.log(Math.ceil(-3.99))   // -3



