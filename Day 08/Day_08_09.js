// Logical Operators


// Logical AND 

let marks = 90 ;

if (marks >= 35 && marks >= 80){
    console.log("PASS");
    console.log("A+");
}



// Logical OR

marks = 75 ;

if (marks >= 33 || marks >= 80){
    console.log("PASS");
    console.log("A+")
}



// Logical NOT

marks = 75 ;

if (!marks < 33){
    console.log("PASS");
    console.log("A+");
}