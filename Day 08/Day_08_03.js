// Operators in JS


// Arithmetic operators 

let a = 10;
let b = 5;

console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);



// Unary operators

console.log(a++)
console.log(++a);



// Assignment operators

a = 5 ;
console.log(a += 1);
console.log(a -= 1);
console.log(a *= 1);
console.log(a /= 1);
console.log(a %= 1);



// Comparison operators

let age = 21;

console.log(age>18);
console.log(age>=18);
console.log(age<18);
console.log(age<=18);
console.log(age==18);
console.log(age!=18);



