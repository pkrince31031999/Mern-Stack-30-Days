// Operations in JS

a = 20
b = 10

sum = a+b 
console.log(sum)
// output : 30

sub = a-b 
console.log(sub)
// output : 10

mult = a*b 
console.log(mult)
// output : 200

div = a/b 
console.log(div)
// output : 2

rem = a%b 
console.log(rem)
// output : 0

expo = 2**3 //exponentiation : power operator
console.log(expo)
// output : 8