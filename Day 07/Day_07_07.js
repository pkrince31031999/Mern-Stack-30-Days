// Keywords in JS



// let Keyword

let a = 5
console.log(a)
// output : 5

let cgpa = 8.5 
cgpa = cgpa *10
console.log(cgpa)
// output : 85



// const Keyword

const num = 5
console.log(num)
// output : 5

const newNum = 10
newNum = 20
console.log(newNum)
// TypeError : Assignment to constant variable



// var Keyword 

var age = 18 
console.log(age)
// output : 18