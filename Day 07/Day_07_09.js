// Assignment Operators in JS


// a = a + 1 == a +=1
let a = 21
a += 1
console.log(a)
// output : 22



// b = b - 1 == b -=1
let b = 21
b -= 1
console.log(b)
// output : 20



// c = c * 2 == c *= 2 
let c = 21
c *= 2
console.log(c)
// output : 42



// d = d / 2 == d /= 2
let d = 20
d /= 2
console.log(d)
// output : 10