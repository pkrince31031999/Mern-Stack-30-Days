// Arrow functions


// syntax : 

const func = (arg1 , arg2) => {
    // function defination 
}




const sum = (a,b) => {
    return a+b ;
}

console.log(sum(5,4));      // 9




const cube = (n) => {
    return n*n*n ;
}

console.log(cube(2));       // 8




const pow = (a,b) => {
    return a**b ;
}

console.log(pow(2,4));      // 16




